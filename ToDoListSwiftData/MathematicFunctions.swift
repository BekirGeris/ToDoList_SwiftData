import Foundation

public class MathematicFunctions {
    
    public init() {
        
    }
    
    public func addIntegers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    public func multiplyIntegers(x: Int, y: Int) -> Int {
        return x * y
    }
    
    public func divideIntegers(x: Int, y: Int) -> Int {
        return x / y
    }
}
